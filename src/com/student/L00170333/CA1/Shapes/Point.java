/**
 * The Point class was created to adhere to the UML provided in the requirements. An earlier version
 * of this solution used java.awt.Point
 */
public class Point {
    public int x;
    public int y;

    public Point() { }

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
